    FOR t IN (
        SELECT table_name
        FROM user_tables
        WHERE table_name LIKE 'TB_ARKIVE_%'
    ) LOOP
        EXECUTE IMMEDIATE
            'DROP TABLE ' || t.table_name || ' CASCADE CONSTRAINTS PURGE';
    END LOOP;

    FOR v IN (
        SELECT view_name
        FROM user_views
        WHERE view_name LIKE '%ARKIVE%'
    ) LOOP
        EXECUTE IMMEDIATE
            'DROP VIEW ' || v.view_name;
    END LOOP;

    FOR tr IN (
        SELECT trigger_name
        FROM user_triggers
        WHERE trigger_name LIKE '%ARKIVE%'
    ) LOOP
        EXECUTE IMMEDIATE
            'DROP TRIGGER ' || tr.trigger_name;
    END LOOP;

    FOR p IN (
        SELECT object_name
        FROM user_procedures
        WHERE object_type = 'PROCEDURE'
          AND object_name LIKE '%ARKIVE%'
    ) LOOP
        EXECUTE IMMEDIATE
            'DROP PROCEDURE ' || p.object_name;
    END LOOP;

    FOR f IN (
        SELECT object_name
        FROM user_procedures
        WHERE object_type = 'FUNCTION'
          AND object_name LIKE '%ARKIVE%'
    ) LOOP
        EXECUTE IMMEDIATE
            'DROP FUNCTION ' || f.object_name;
    END LOOP;

    FOR s IN (
        SELECT sequence_name
        FROM user_sequences
        WHERE sequence_name LIKE '%ARKIVE%'
          AND sequence_name NOT LIKE 'ISEQ$$_%'
    ) LOOP
        EXECUTE IMMEDIATE
            'DROP SEQUENCE ' || s.sequence_name;
    END LOOP;

END;
